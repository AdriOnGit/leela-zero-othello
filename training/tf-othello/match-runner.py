@dataclass
class MatchRunner:
    A: Engine
    B: Engine
    games: int = 100
    root: Path = Path(results_root)

    def run(self) -> Tuple[str, List[Dict]]:
        assert self.games % 2 == 0, "games must be even"
        mid = uuid4().hex[:8]
        tmp = self.root / "tmp" / mid
        tmp.mkdir(parents=True, exist_ok=True)

        print(f"Starting match {mid}")
        rows: List[Dict] = []
        for g in range(self.games):
            blk, wht = (self.A, self.B) if g % 2 == 0 else (self.B, self.A)
            result = Game(blk, wht, g + 1, mid, tmp).play()
            winner = blk.name if result.res_a.startswith('B') else wht.name
            print(f"Game {g+1} won by {winner} with score {result.res_a}")
            rows.append({
                "match_id": mid,
                "game_no": g + 1,
                "black": blk.name,
                "white": wht.name,
                f"{self.A.name}_result": result.res_a,
                f"{self.B.name}_result": result.res_b,
                "move_count": result.moves,
                "sgf_file": result.sgf.name,
            })

        # tar.gz of SGFs copied under both categories
        tar_name = f"match_{mid}.tar.gz"
        tar_tmp = tmp.parent / tar_name
        with tarfile.open(tar_tmp, "w:gz") as tar:
            for f in tmp.glob("*.sgf"):
                tar.add(f, arcname=f.name)

        for cat in (self.A.category, self.B.category):
            dest = self.root / cat
            dest.mkdir(parents=True, exist_ok=True)
            (dest / tar_name).write_bytes(tar_tmp.read_bytes())

        # clean tmp
        for f in tmp.glob("*"):
            f.unlink()
        tmp.rmdir()

        return mid, rows


# ---------------------------------------------------------------------------
# Batch helper
# ---------------------------------------------------------------------------

def _work(spec):
    A, B, n, r = spec
    mid, rows = MatchRunner(A, B, n, Path(r)).run()
    return rows


def run_batch(pairs: List[Tuple[Engine, Engine]], games: int = 100, workers: int = 1, csv_out: str | Path | None = None):
    if csv_out is None:
        csv_out = Path(results_root) / "results.csv"

    specs = [(a, b, games, str(results_root)) for a, b in pairs]
    all_rows: List[Dict] = []

    if workers == 1:
        for s in specs:
            all_rows.extend(_work(s))
    else:
        with Pool(min(cpu_count(), workers)) as pool:
            for rows in pool.imap_unordered(_work, specs):
                all_rows.extend(rows)

    # write CSV
    fields = [
        "match_id", "game_no", "black", "white",
        *sorted({k for r in all_rows for k in r if k.endswith("_result")}),
        "move_count", "sgf_file",
    ]
    with open(csv_out, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        writer.writerows(all_rows)

    print(f"Batch finished – csv saved to {csv_out}")


