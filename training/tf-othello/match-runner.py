_ALPHA = "abcdefgh"

def coord_to_sgf(m: str) -> str:
    if m.lower() == "pass":
        return ""
    x = _ALPHA.index(m[0].lower())
    y = int(m[1]) - 1
    return f"{_ALPHA[x]}{_ALPHA[y]}"


# ---------------------------------------------------------------------------
# Game driver
# ---------------------------------------------------------------------------

@dataclass
class GameResult:
    moves: int
    res_a: str
    res_b: str
    sgf: Path


class Game:
    def __init__(self, black: Engine, white: Engine, no: int, match_id: str, out_dir: Path):
        self.B = black
        self.W = white
        self.no = no
        self.mid = match_id
        self.out = out_dir

    def _sgf(self, seq, res_tag):
        parts = [SGF_HEADER, f"PB[{self.B.name}]", f"PW[{self.W.name}]", f"RE[{res_tag}]"]
        for col, mv in seq:
            if mv.lower() == "resign":
                continue
            parts.append(f";{col}[{coord_to_sgf(mv)}]")
        parts.append(SGF_FOOTER)
        return "".join(parts)

    def play(self) -> GameResult:
        # fresh engines
        self.B.restart()
        self.W.restart()

        history = []  # ("B"|"W", move)
        to_move = "black"
        passes = 0
        moves = 0
        resigned = False

        while True:
            eng = self.B if to_move == "black" else self.W
            opp = self.W if to_move == "black" else self.B

            mv = eng.genmove(to_move)
            moves += 1
            history.append(("B" if to_move == "black" else "W", mv))

            lo = mv.lower()
            # mirror the move to the opponent so its board state matches
            if lo == "pass":
                opp.play_move(to_move, "pass")
            elif lo != "resign":
                opp.play_move(to_move, mv)

            if lo == "resign":
                resigned = True
                break
            if lo == "pass":
                passes += 1
                if passes == 2:
                    break
            else:
                passes = 0

            to_move = "white" if to_move == "black" else "black"

        if resigned:
            res_a = res_b = "W+R" if to_move == "black" else "B+R"
        else:
            res_a = self.B.final_score()
            res_b = self.W.final_score()

        sgf_txt = self._sgf(history, res_a)
        fn = f"{self.mid}_g{self.no:03d}_{self.B.short()}-B_{self.W.short()}-W.sgf"
        fp = self.out / fn
        with open(fp, "w", encoding="utf-8") as f:
            f.write(sgf_txt)

        self.B.close()
        self.W.close()
        return GameResult(moves, res_a, res_b, fp)


